#include "minishell.h"

void	add_string(char *str1, const char *str2)
{
	int	i;
	int	u;

	i = 0;
	u = 0;
	while (str1[i])
		i++;
	while (str2[u])
		str1[i++] = str2[u++];
	str1[i] = '\0';
}
