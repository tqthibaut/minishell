#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	size_t	i;

	i = 0;
	if (!dest && !src)
		return (NULL);
	if (dest > src)
	{
		while (i < n && ((unsigned char *)dest)[i] && dest != src + i)
			i++;
		if (i < n && dest == src + i)
		{
			while (0 < n--)
				((unsigned char *)dest)[n] = ((unsigned char *)src)[n];
			return (dest);
		}
	}
	i = 0;
	while (i < n)
	{
		((unsigned char *)dest)[i] = ((unsigned char *)src)[i];
		i++;
	}
	return (dest);
}
